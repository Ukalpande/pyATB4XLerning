#sub of two number
def sub_of_two_number(a,b):
    return a-b
result_sub1 = sub_of_two_number(40,30)
result_sub2 = sub_of_two_number(100,30)
print(result_sub1)
print(result_sub2)

#multiplication of two number

def mul_of_two_number(a,b):
    return a*b
result_mul1 = mul_of_two_number(2,5)
result_mul2 = mul_of_two_number(6,10)
print(result_mul1)
